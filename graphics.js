function Tile(pathH, pathF, desc, imgX, imgY, imgWidth, imgHeight) {
	var _self = this;
	var loadedH = false;

	this.imageH 		= new Image();
	this.imageH.src 	= pathH;
	this.desc 			= desc;
	this.imageH.onload 	= function() { loadedH = true; };

	var loadedF = false;

	this.imageF 		= new Image();
	this.imageF.src 	= pathF;
	this.desc 			= desc;
	this.imageF.onload 	= function() { loadedF = true; };

	this.imgX 		= imgX;
	this.imgY 		= imgY;
	this.imgWidth 	= imgWidth;
	this.imgHeight 	= imgHeight;
}
